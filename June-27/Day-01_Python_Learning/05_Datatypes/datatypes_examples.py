# Integer
age = 25
print("Age:", age)
print("Type of age:", type(age))

# Float
height = 5.9
print("Height:", height)
print("Type of height:", type(height))

# String
name = "Alice"
print("Name:", name)
print("Type of name:", type(name))


# List
colors = ["red", "green", "blue"]
print("Colors:", colors)
print("Type of colors:", type(colors))

# Tuple
coordinates = (10.5, 22.8)
print("Coordinates:", coordinates)
print("Type of coordinates:", type(coordinates))

# Dictionary
person = {"name": "Bob", "age": 30}
print("Person:", person)
print("Type of person:", type(person))

# Boolean
is_student = True
print("Is student:", is_student)
print("Type of is_student:", type(is_student))

# Set
fruits = {"apple", "banana", "orange"}
print("Fruits:", fruits)
print("Type of fruits:", type(fruits))

# Complex
complex_number = 3 + 4j
complex_number2 = complex(3, 4)
print("Complex number:", complex_number)
print("Complex number2:", complex_number2)
print("Type of complex_number:", type(complex_number))

# Bytes
data = b"Hello"
print("Data:", data)
print("Type of data:", type(data))

# Bytearray
data = bytearray(b"Hello")
print("Data:", data)
print("Type of data:", type(data))