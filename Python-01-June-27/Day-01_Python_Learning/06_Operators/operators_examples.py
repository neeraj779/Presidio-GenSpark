# Arithmetic operators
a = 10
b = 5
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Exponentiation:", a ** b)
print("Floor division:", a // b)

# Comparison operators
print("Equal:", a == b)
print("Not equal:", a != b)
print("Greater than:", a > b)
print("Less than:", a < b)
print("Greater than or equal to:", a >= b)
print("Less than or equal to:", a <= b)

# Logical operators
x = True
y = False
print("Logical AND:", x and y)
print("Logical OR:", x or y)
print("Logical NOT:", not x)

# Assignment operators
c = 5
c += 2
print("c after += 2:", c)
c -= 1
print("c after -= 1:", c)
c *= 3
print("c after *= 3:", c)
c /= 2
print("c after /= 2:", c)

# Bitwise operators
d = 6  # binary: 110
e = 2  # binary: 010
print("Bitwise AND:", d & e)  # binary: 010
print("Bitwise OR:", d | e)   # binary: 110
print("Bitwise XOR:", d ^ e)  # binary: 100
print("Bitwise NOT:", ~d)     # binary: -111
print("Bitwise left shift:", d << 1)  # binary: 1100
print("Bitwise right shift:", d >> 1)  # binary: 011
